# Program untuk menghitung luas layang-layang

# Input dari user
d1 = float(input("Masukkan nilai diagonal 1 layang-layang: "))
d2 = float(input("Masukkan nilai diagonal 2 layang-layang: "))

# Menghitung luas layang-layang
luas = (d1 * d2) / 2

# Menampilkan hasil
print("Luas layang-layang adalah", luas)
