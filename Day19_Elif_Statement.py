nama = input("Nama kamyu siapa? ")

if nama == "chaerul": # kondisi 1
	print("Hai chaerul!") # aksi true 1
elif nama == "irul": # kondisi 2
	print("Hai si kece bangeeeets!!") # aksi true 2
elif nama == "rul": # kondisi 3
	print("Hai rul!") # aksi true 3
else:
	print("au ah gak kenal!!!") # aksi false

