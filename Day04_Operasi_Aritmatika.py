# Operasi Aritmatika pada python

a = 10
b = 6

# Penjumlahan ( + )
print("========== Operasi Penjumlahan =======")
hasil = a + b
print(a,'+' ,b, '=', hasil)

# Pengurangan ( - )
print("========== Operasi Pengurangan =======")
hasil = a - b
print(a,'-' ,b, '=', hasil)

# Perkalian ( * )
print("========== Operasi Perkalian =======")
hasil = a * b
print(a,'x' ,b, '=', hasil)

# Penjumlahan ( / ) ket: untuk operasi pembagian otomatis outputnya jadi float
print("========== Operasi Pembagian =======")
hasil = a / b
print(a,'/' ,b, '=', hasil)

# Modulus (%) ket.modulus adalah sisa bagi
print("========== Operasi Modulus =======")
hasil = a ** b
print(a,'%' ,b, '=', hasil)

# Eksponen (pangkat) **
print("========== Operasi Eksponen =======")
hasil = a ** b
print(a,'**' ,b, '=', hasil)
