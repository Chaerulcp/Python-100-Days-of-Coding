# Membuat matriks 3x3 menggunakan list
matriks = [[1, 2, 3],
           [4, 5, 6],
           [7, 8, 9]]

# Menampilkan matriks ke layar
print(matriks)

# Menampilkan elemen di baris ke-2 kolom ke-3
print(matriks[1][2])

# Mengubah elemen di baris ke-3 kolom ke-1 menjadi 10
matriks[2][0] = 10

# Menampilkan matriks ke layar setelah diubah
print(matriks)
