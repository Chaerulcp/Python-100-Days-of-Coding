# Meminta input panjang alas dan tinggi dari pengguna
alas = float(input("Masukkan panjang alas: "))
tinggi = float(input("Masukkan tinggi jajar genjang: "))

# Menghitung luas jajar genjang menggunakan rumus di atas
luas = alas * tinggi

# Menampilkan hasilnya ke layar
print("Luas jajar genjang adalah", luas)
