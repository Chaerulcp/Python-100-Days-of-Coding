nama = input("Siapa nama anda? ")

# 1. program if inline
if nama=="chaerul" : print("Kamu keren!!!!")
print(f"Terima kasih {nama}")

# 2. program if indentation

if nama=="chaerul":
	print("kamu keren abieeeez!")
	print("kamu juga keren banget!")
print(f"Terima kasih {nama}")

# 3. Else Statement

if nama=="chaerul":
	print("hai chaerul, si keren!!!")
else:
	print("Ah kamu bukan chaerul, kamu gak keren! :(")

